
public class Kalkulator {
	//atribut untuk class kalkulator
	public int bil1;
	public int bil2;
	
	//Method untuk penjumlahan
	public int penjumlahan(int bil1, int bil2) {
		return (bil1 + bil2);
	}
	
	//Method untuk Pengurangan
	public int pengurangan(int bil1, int bil2) {
		return (bil1 - bil2);
	}
	
	//Method untuk Perkalian
	public int perkalian(int bil1, int bil2) {
		return (bil1 * bil2);
	}
	
	//Method untuk Pembagian
	public double pembagian(int bil1, int bil2) {
		return (bil1/bil2);
	}
}
